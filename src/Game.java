public class Game {
    int id;
    String name;
    String genre;
    String platform;
    int releaseYear;
    String author;


    public Game(int id, String name, String genre, String platform, int releaseYear, String author) {
        this.id = id;
        this.name = name;
        this.genre = genre;
        this.platform = platform;
        this.releaseYear = releaseYear;
        this.author = author;
    }

}